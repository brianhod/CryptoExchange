LimitCheckResult PositionLimits::checkSymbolLimit(
    const Symbol& symbol,
    double newQuantity,
    double price
) const {
    std::shared_lock lock(mutex_);

    LimitCheckResult result;
    result.passed = true;
    result.limitName = std::string(symbol.view()) + "_position";

    // Get limit for symbol (or use default)
    SymbolPositionLimit limit = defaultSymbolLimit_;
    auto it = symbolLimits_.find(std::string(symbol.view()));
    if (it != symbolLimits_.end()) {
        limit = it->second;
    }

    if (!limit.enabled) {
        return result; // Limit disabled, always pass
    }

    // Get current position value
    double currentValue = getCurrentSymbolValue(symbol);
    double newValue = std::abs(newQuantity) * price;

    // Check based on limit type
    switch (limit.type) {
        case LimitType::Notional:
            result.currentValue = currentValue + newValue;
            result.limitValue = limit.maxNotional.value_or(limit.limit);
            break;

        case LimitType::Quantity:
            result.currentValue = std::abs(newQuantity);
            result.limitValue = limit.limit;
            break;

        case LimitType::Percentage:
            {
                double totalValue = getCurrentPortfolioValue();
                result.currentValue = totalValue > 0 ?
                    ((currentValue + newValue) / totalValue) * 100.0 : 0.0;
                result.limitValue = limit.maxConcentrationPct.value_or(limit.limit);
            }
            break;
    }

    result.utilizationPct = result.limitValue > 0 ?
        (result.currentValue / result.limitValue) * 100.0 : 0.0;

    if (result.currentValue > result.limitValue) {
        result.passed = false;
        result.message = "Symbol position limit exceeded";
    } else if (result.currentValue > limit.getWarningLevel()) {
        result.message = "Symbol position approaching limit";
    }

    return result;
}

LimitCheckResult PositionLimits::checkExchangeLimit(
    Exchange exchange,
    double additionalValue
) const {
    std::shared_lock lock(mutex_);

    LimitCheckResult result;
    result.passed = true;
    result.limitName = std::string(exchange_to_string(exchange)) + "_position";

    // Get limit for exchange (or use default)
    ExchangePositionLimit limit = defaultExchangeLimit_;
    auto it = exchangeLimits_.find(exchange);
    if (it != exchangeLimits_.end()) {
        limit = it->second;
    }

    if (!limit.enabled) {
        return result;
    }

    double currentValue = getCurrentExchangeValue(exchange);
    result.currentValue = currentValue + additionalValue;
    result.limitValue = limit.maxPositionValue;
    result.utilizationPct = result.limitValue > 0 ?
        (result.currentValue / result.limitValue) * 100.0 : 0.0;

    if (result.currentValue > result.limitValue) {
        result.passed = false;
        result.message = "Exchange position limit exceeded";
    } else if (result.currentValue > limit.getWarningLevel()) {
        result.message = "Exchange position approaching limit";
    }

    return result;
}

LimitCheckResult PositionLimits::checkPortfolioLimits(double additionalValue) const {
    std::shared_lock lock(mutex_);

    LimitCheckResult result;
    result.passed = true;
    result.limitName = "portfolio_total";

    double currentValue = getCurrentPortfolioValue();
    result.currentValue = currentValue + additionalValue;
    result.limitValue = portfolioLimits_.maxTotalPositionValue;
    result.utilizationPct = result.limitValue > 0 ?
        (result.currentValue / result.limitValue) * 100.0 : 0.0;

    if (result.currentValue > result.limitValue) {
        result.passed = false;
        result.message = "Portfolio position limit exceeded";
    }

    // Check open positions count
    if (positionManager_) {
        size_t openCount = positionManager_->getOpenPositionCount();
        if (openCount >= portfolioLimits_.maxOpenPositions) {
            result.passed = false;
            result.message = "Maximum open positions reached";
        }
    }

    return result;
}

std::vector<LimitCheckResult> PositionLimits::checkAllLimits(
    const Symbol& symbol,
    Exchange exchange,
    Side side,
    double quantity,
    double price
) const {
    std::vector<LimitCheckResult> results;

    double notional = quantity * price;

    // Check symbol limit
    results.push_back(checkSymbolLimit(symbol, quantity, price));

    // Check exchange limit
    results.push_back(checkExchangeLimit(exchange, notional));

    // Check portfolio limits
    results.push_back(checkPortfolioLimits(notional));

    // Check if symbol is allowed on exchange
    if (!isSymbolAllowedOnExchange(symbol, exchange)) {
        LimitCheckResult result;
        result.passed = false;
        result.limitName = "symbol_exchange_allowed";
        result.message = "Symbol not allowed on this exchange";
        results.push_back(result);
    }

    return results;
}

bool PositionLimits::isSymbolAllowedOnExchange(const Symbol& symbol, Exchange exchange) const {
    std::shared_lock lock(mutex_);

    auto it = exchangeLimits_.find(exchange);
    if (it == exchangeLimits_.end()) {
        return true; // No limit configured, allow all
    }

    const auto& limit = it->second;
    std::string symbolStr(symbol.view());

    // Check blocked list
    if (std::find(limit.blockedSymbols.begin(), limit.blockedSymbols.end(),
                  symbolStr) != limit.blockedSymbols.end()) {
        return false;
    }

    // Check allowed list (empty = all allowed)
    if (!limit.allowedSymbols.empty()) {
        return std::find(limit.allowedSymbols.begin(), limit.allowedSymbols.end(),
                         symbolStr) != limit.allowedSymbols.end();
    }

    return true;
}

double PositionLimits::getSymbolUtilization(const Symbol& symbol) const {
    std::shared_lock lock(mutex_);

    double currentValue = getCurrentSymbolValue(symbol);

    SymbolPositionLimit limit = defaultSymbolLimit_;
    auto it = symbolLimits_.find(std::string(symbol.view()));
    if (it != symbolLimits_.end()) {
        limit = it->second;
    }

    return limit.limit > 0 ? (currentValue / limit.limit) * 100.0 : 0.0;
}

double PositionLimits::getExchangeUtilization(Exchange exchange) const {
    std::shared_lock lock(mutex_);

    double currentValue = getCurrentExchangeValue(exchange);

    ExchangePositionLimit limit = defaultExchangeLimit_;
    auto it = exchangeLimits_.find(exchange);
    if (it != exchangeLimits_.end()) {
        limit = it->second;
    }

    return limit.maxPositionValue > 0 ?
        (currentValue / limit.maxPositionValue) * 100.0 : 0.0;
}

double PositionLimits::getPortfolioUtilization() const {
    std::shared_lock lock(mutex_);

    double currentValue = getCurrentPortfolioValue();
    return portfolioLimits_.maxTotalPositionValue > 0 ?
        (currentValue / portfolioLimits_.maxTotalPositionValue) * 100.0 : 0.0;
}

std::unordered_map<std::string, double> PositionLimits::getAllUtilizations() const {
    std::unordered_map<std::string, double> utilizations;

    utilizations["portfolio"] = getPortfolioUtilization();

    for (const auto& [symbol, limit] : symbolLimits_) {
        utilizations["symbol_" + symbol] = getSymbolUtilization(Symbol(symbol));
    }

    for (const auto& [exchange, limit] : exchangeLimits_) {
        utilizations["exchange_" + std::string(exchange_to_string(exchange))] =
            getExchangeUtilization(exchange);
    }

    return utilizations;
}

double PositionLimits::getSymbolHeadroom(const Symbol& symbol, Side side) const {
    std::shared_lock lock(mutex_);

    double currentValue = getCurrentSymbolValue(symbol);

    SymbolPositionLimit limit = defaultSymbolLimit_;
    auto it = symbolLimits_.find(std::string(symbol.view()));
    if (it != symbolLimits_.end()) {
        limit = it->second;
    }

    return std::max(0.0, limit.limit - currentValue);
}

double PositionLimits::getExchangeHeadroom(Exchange exchange) const {
    std::shared_lock lock(mutex_);

    double currentValue = getCurrentExchangeValue(exchange);

    ExchangePositionLimit limit = defaultExchangeLimit_;
    auto it = exchangeLimits_.find(exchange);
    if (it != exchangeLimits_.end()) {
        limit = it->second;
    }

    return std::max(0.0, limit.maxPositionValue - currentValue);
}

double PositionLimits::getPortfolioHeadroom() const {
    std::shared_lock lock(mutex_);

    double currentValue = getCurrentPortfolioValue();
    return std::max(0.0, portfolioLimits_.maxTotalPositionValue - currentValue);
}

void PositionLimits::setPositionManager(std::shared_ptr<PositionManager> positionManager) {
    positionManager_ = std::move(positionManager);
}

std::shared_ptr<PositionManager> PositionLimits::getPositionManager() const {
    return positionManager_;
}

double PositionLimits::getCurrentSymbolValue(const Symbol& symbol) const {
    if (!positionManager_) return 0.0;

    auto positions = positionManager_->getPositionsForSymbol(symbol);
    double total = 0.0;
    for (const auto& pos : positions) {
        total += pos.notionalValue();
    }
    return total;
}

double PositionLimits::getCurrentExchangeValue(Exchange exchange) const {
    if (!positionManager_) return 0.0;

    auto positions = positionManager_->getPositionsForExchange(exchange);
    double total = 0.0;
    for (const auto& pos : positions) {
        total += pos.notionalValue();
    }
    return total;
}

double PositionLimits::getCurrentPortfolioValue() const {
    if (!positionManager_) return 0.0;
    return positionManager_->getGrossExposure();
}

