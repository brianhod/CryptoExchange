PositionLimitsBuilder& PositionLimitsBuilder::withPositionManager(
    std::shared_ptr<PositionManager> pm) {
    positionManager_ = std::move(pm);
    return *this;
}

PositionLimitsBuilder& PositionLimitsBuilder::withSymbolLimit(
    const Symbol& symbol, double maxNotional) {
    SymbolPositionLimit limit;
    limit.symbol = symbol;
    limit.limit = maxNotional;
    limit.maxNotional = maxNotional;
    limit.type = LimitType::Notional;
    limit.enabled = true;
    symbolLimits_.emplace_back(symbol, limit);
    return *this;
}

PositionLimitsBuilder& PositionLimitsBuilder::withExchangeLimit(
    Exchange exchange, double maxValue) {
    ExchangePositionLimit limit;
    limit.exchange = exchange;
    limit.maxPositionValue = maxValue;
    limit.limit = maxValue;
    limit.type = LimitType::Notional;
    limit.enabled = true;
    exchangeLimits_.emplace_back(exchange, limit);
    return *this;
}

PositionLimitsBuilder& PositionLimitsBuilder::withPortfolioLimits(
    const PortfolioLimits& limits) {
    portfolioLimits_ = limits;
    return *this;
}

PositionLimitsBuilder& PositionLimitsBuilder::loadFromYaml(
    const std::filesystem::path& path) {
    yamlPath_ = path;
    return *this;
}

std::unique_ptr<PositionLimits> PositionLimitsBuilder::build() {
    auto limits = std::make_unique<PositionLimits>(positionManager_);

    limits->setPortfolioLimits(portfolioLimits_);

    for (const auto& [symbol, limit] : symbolLimits_) {
        limits->setSymbolLimit(symbol, limit);
    }

    for (const auto& [exchange, limit] : exchangeLimits_) {
        limits->setExchangeLimit(exchange, limit);
    }

    if (yamlPath_) {
        limits->loadFromYaml(*yamlPath_);
    }

    return limits;
}

