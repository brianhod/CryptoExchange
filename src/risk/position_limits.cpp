PositionLimits::PositionLimits() {
    // Set default limits
    defaultSymbolLimit_.limit = 1000000.0;
    defaultSymbolLimit_.type = LimitType::Notional;
    defaultSymbolLimit_.warningThreshold = 0.8;
    defaultSymbolLimit_.enabled = true;

    defaultExchangeLimit_.limit = 5000000.0;
    defaultExchangeLimit_.type = LimitType::Notional;
    defaultExchangeLimit_.warningThreshold = 0.8;
    defaultExchangeLimit_.maxPositionValue = 5000000.0;
    defaultExchangeLimit_.maxOpenOrders = 100;
    defaultExchangeLimit_.enabled = true;

    LOG_INFO("PositionLimits initialized with default limits");
}

PositionLimits::PositionLimits(std::shared_ptr<PositionManager> positionManager)
    : PositionLimits()
{
    positionManager_ = std::move(positionManager);
}

PositionLimits::~PositionLimits() = default;

bool PositionLimits::loadFromYaml(const std::filesystem::path& configPath) {
    try {
        std::ifstream file(configPath);
        if (!file.is_open()) {
            LOG_ERROR("Failed to open config file: {}", configPath.string());
            return false;
        }

        std::stringstream buffer;
        buffer << file.rdbuf();
        return loadFromYamlString(buffer.str());

    } catch (const std::exception& e) {
        LOG_ERROR("Failed to load YAML config: {}", e.what());
        return false;
    }
}

bool PositionLimits::saveToYaml(const std::filesystem::path& configPath) const {
    try {
        std::ofstream file(configPath);
        if (!file.is_open()) {
            LOG_ERROR("Failed to open config file for writing: {}", configPath.string());
            return false;
        }

        std::shared_lock lock(mutex_);

        file << "# Position Limits Configuration\n";
        file << "# Generated by HFT Position Limits Manager\n\n";

        // Portfolio limits
        file << "portfolio:\n";
        file << "  max_total_position_value: " << portfolioLimits_.maxTotalPositionValue << "\n";
        file << "  max_gross_exposure: " << portfolioLimits_.maxGrossExposure << "\n";
        file << "  max_net_exposure: " << portfolioLimits_.maxNetExposure << "\n";
        file << "  max_leverage: " << portfolioLimits_.maxLeverage << "\n";
        file << "  max_open_positions: " << portfolioLimits_.maxOpenPositions << "\n";
        file << "  max_concentration_pct: " << portfolioLimits_.maxConcentrationPct << "\n\n";

        // Symbol limits
        file << "symbols:\n";
        for (const auto& [symbol, limit] : symbolLimits_) {
            file << "  " << symbol << ":\n";
            file << "    limit: " << limit.limit << "\n";
            file << "    type: " << (limit.type == LimitType::Notional ? "notional" :
                                     limit.type == LimitType::Quantity ? "quantity" : "percentage") << "\n";
            file << "    warning_threshold: " << limit.warningThreshold << "\n";
            file << "    enabled: " << (limit.enabled ? "true" : "false") << "\n";
            if (limit.maxLongQuantity) {
                file << "    max_long_quantity: " << *limit.maxLongQuantity << "\n";
            }
            if (limit.maxShortQuantity) {
                file << "    max_short_quantity: " << *limit.maxShortQuantity << "\n";
            }
            if (limit.maxNotional) {
                file << "    max_notional: " << *limit.maxNotional << "\n";
            }
        }
        file << "\n";

        // Exchange limits
        file << "exchanges:\n";
        for (const auto& [exchange, limit] : exchangeLimits_) {
            file << "  " << exchange_to_string(exchange) << ":\n";
            file << "    max_position_value: " << limit.maxPositionValue << "\n";
            file << "    max_open_orders: " << limit.maxOpenOrders << "\n";
            file << "    enabled: " << (limit.enabled ? "true" : "false") << "\n";
            if (!limit.blockedSymbols.empty()) {
                file << "    blocked_symbols: [";
                for (size_t i = 0; i < limit.blockedSymbols.size(); ++i) {
                    if (i > 0) file << ", ";
                    file << limit.blockedSymbols[i];
                }
                file << "]\n";
            }
        }

        LOG_INFO("Saved position limits to: {}", configPath.string());
        return true;

    } catch (const std::exception& e) {
        LOG_ERROR("Failed to save YAML config: {}", e.what());
        return false;
    }
}

bool PositionLimits::loadFromYamlString(const std::string& yamlContent) {
    // Simple YAML-like parsing without external dependency
    // In production, use yaml-cpp for proper parsing
    try {
        std::unique_lock lock(mutex_);

        std::istringstream stream(yamlContent);
        std::string line;
        std::string currentSection;
        std::string currentItem;

        while (std::getline(stream, line)) {
            // Skip comments and empty lines
            size_t commentPos = line.find('#');
            if (commentPos != std::string::npos) {
                line = line.substr(0, commentPos);
            }

            // Trim whitespace
            size_t start = line.find_first_not_of(" \t");
            if (start == std::string::npos) continue;
            line = line.substr(start);

            if (line.empty()) continue;

            // Detect sections
            if (line == "portfolio:") {
                currentSection = "portfolio";
                continue;
            } else if (line == "symbols:") {
                currentSection = "symbols";
                continue;
            } else if (line == "exchanges:") {
                currentSection = "exchanges";
                continue;
            }

            // Parse key-value pairs
            size_t colonPos = line.find(':');
            if (colonPos != std::string::npos) {
                std::string key = line.substr(0, colonPos);
                std::string value = (colonPos + 1 < line.size()) ?
                    line.substr(colonPos + 1) : "";

                // Trim key and value
                size_t keyEnd = key.find_last_not_of(" \t");
                if (keyEnd != std::string::npos) key = key.substr(0, keyEnd + 1);

                size_t valStart = value.find_first_not_of(" \t");
                if (valStart != std::string::npos) value = value.substr(valStart);

                // Handle indented keys (section items)
                size_t indent = line.find_first_not_of(" \t");

                if (currentSection == "portfolio") {
                    if (key == "max_total_position_value") {
                        portfolioLimits_.maxTotalPositionValue = std::stod(value);
                    } else if (key == "max_gross_exposure") {
                        portfolioLimits_.maxGrossExposure = std::stod(value);
                    } else if (key == "max_net_exposure") {
                        portfolioLimits_.maxNetExposure = std::stod(value);
                    } else if (key == "max_leverage") {
                        portfolioLimits_.maxLeverage = std::stod(value);
                    } else if (key == "max_open_positions") {
                        portfolioLimits_.maxOpenPositions = std::stoull(value);
                    } else if (key == "max_concentration_pct") {
                        portfolioLimits_.maxConcentrationPct = std::stod(value);
                    }
                }
                // Additional parsing for symbols and exchanges would go here
            }
        }

        LOG_INFO("Loaded position limits from YAML");
        return true;

    } catch (const std::exception& e) {
        LOG_ERROR("Failed to parse YAML: {}", e.what());
        return false;
    }
}

void PositionLimits::setSymbolLimit(const Symbol& symbol, const SymbolPositionLimit& limit) {
    std::unique_lock lock(mutex_);
    symbolLimits_[std::string(symbol.view())] = limit;
    LOG_INFO("Set symbol limit: {} = {}", symbol.view(), limit.limit);
}

std::optional<SymbolPositionLimit> PositionLimits::getSymbolLimit(const Symbol& symbol) const {
    std::shared_lock lock(mutex_);

    auto it = symbolLimits_.find(std::string(symbol.view()));
    if (it != symbolLimits_.end()) {
        return it->second;
    }
    return std::nullopt;
}

void PositionLimits::removeSymbolLimit(const Symbol& symbol) {
    std::unique_lock lock(mutex_);
    symbolLimits_.erase(std::string(symbol.view()));
    LOG_INFO("Removed symbol limit: {}", symbol.view());
}

std::vector<SymbolPositionLimit> PositionLimits::getAllSymbolLimits() const {
    std::shared_lock lock(mutex_);

    std::vector<SymbolPositionLimit> result;
    result.reserve(symbolLimits_.size());

    for (const auto& [symbol, limit] : symbolLimits_) {
        result.push_back(limit);
    }

    return result;
}

